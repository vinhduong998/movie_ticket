import {Dimensions, StyleSheet} from 'react-native';

const {width, height} = Dimensions.get('window');
const [shortDimension, longDimension] = width < height ? [width, height] : [height, width];

/**
 * Default guideline sizes are based on standard ~5" screen mobile device
 */

const guidelineBaseWidth = 375;
const guidelineBaseHeight = 680;

export const HIT_SLOP_EXPAND_20 = { top: 20, left: 20, right: 20, bottom: 20 };
export const HIT_SLOP_EXPAND_10 = { top: 10, left: 10, right: 10, bottom: 10 };

/**
 * @param size
 * Scale by screen horizontal ratio
 */
export const horizontalScale = (size: number) => shortDimension / guidelineBaseWidth * size;

/**
 * @param size
 * Scale by screen vertical ratio
 */
export const verticalScale = (size: number) => longDimension / guidelineBaseHeight * size;

/**
 *
 * @param size
 * @param factor
 * Scale by screen horizontal ratio with factor for size compensation. Default factor is 0.5.
 */
export const moderateHorizontalScale = (size: number, factor = 0.5) => size + (horizontalScale(size) - size) * factor;

/**
 *
 * @param size
 * @param factor
 * Scale by screen vertical ratio with factor for size compensation. Default factor is 0.5.
 */
export const moderateVerticalScale = (size: number, factor = 0.5) => size + (verticalScale(size) - size) * factor;

export const hs = horizontalScale;
export const vs = verticalScale;
export const mhs = moderateHorizontalScale;
export const mvs = moderateVerticalScale;

